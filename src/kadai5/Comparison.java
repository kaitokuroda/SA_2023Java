package kadai5;

public class Comparison {
    int getMax(int a, int b) {
        if (a >= b) {
            return a;
        } else {
            return b;
        }
    }

    int getMin(int a, int b) {
        if (a <= b) {
            return a;
        } else {
            return b;
        }
    }
}
